package com.ojcoleman.europa.transcribers.nn;

/**
 * Collection of constants used to refer to parts of a neural network.
 */
public enum NNPart {
	/**
	 * Represents a type of neuron (rather than an actual neuron).
	 */
	NEURON_TYPE,
	/**
	 * Represents a type of synapse (rather than an actual synapse).
	 */
	SYNAPSE_TYPE,
	/**
	 * Represents a neuron.
	 */
	NEURON,
	/**
	 * Represents an input neuron.
	 */
	NEURON_INPUT,
	/**
	 * Represents a hidden or internal neuron.
	 */
	NEURON_HIDDEN,
	/**
	 * Represents an output neuron.
	 */
	NEURON_OUTPUT,
	/**
	 * Represents a synapse.
	 */
	SYNAPSE
}
